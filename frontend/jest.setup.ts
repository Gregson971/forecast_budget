// Mock IntersectionObserver
global.IntersectionObserver = class IntersectionObserver {
  constructor() {}
  disconnect() {}
  observe() {}
  takeRecords() {
    return [];
  }
  unobserve() {}
} as any;

// Suppress unhandled promise rejections in tests
// (they should be handled by error state in hooks/components)
process.on('unhandledRejection', (reason: any) => {
  const message = reason?.message || String(reason);
  // Suppress expected test errors
  if (
    message.includes('Failed to fetch') ||
    message.includes('Failed to revoke') ||
    message.includes('Test error')
  ) {
    return; // Silently ignore expected test errors
  }
  // Log unexpected errors
  console.error('Unhandled promise rejection:', reason);
});
